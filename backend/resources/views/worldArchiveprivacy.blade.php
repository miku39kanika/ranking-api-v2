    # プライバシーポリシー

    最終更新日：2026年6月15日

    World Archive（以下「本アプリ」）は、ユーザーのプライバシーを尊重し、個人情報の保護に努めます。

    ---

    # 1. 取得する情報

    本アプリは、ユーザーの個人情報を収集しません。

    また、氏名、メールアドレス、位置情報、広告識別子等の情報を取得することはありません。

    ---

    # 2. 保存されるデータについて

    本アプリで作成した世界観データ、キャラクター情報、組織情報、アイテム情報、出来事情報、地図情報等は、ユーザーの端末内にのみ保存されます。

    これらのデータが運営者のサーバーへ送信されることはありません。

    ---

    # 3. 写真へのアクセス

    本アプリでは、キャラクター画像や地図画像などを登録するために、ユーザーが選択した写真へアクセスする場合があります。

    選択された画像は端末内でのみ利用され、外部へ送信されることはありません。

    ---

    # 4. 写真への保存

    本アプリでは、相関図や地図などを画像として保存するために、写真ライブラリへの書き込みを行う場合があります。

    保存処理はユーザーの操作によってのみ実行されます。

    ---

    # 5. 第三者提供

    本アプリは、ユーザー情報を第三者へ提供しません。

    ---

    # 6. 外部サービス

    本アプリは、広告配信サービス、アクセス解析サービス、外部サーバーサービスを利用していません。

    ---

    # 7. ポリシーの変更

    運営者は、必要に応じて本ポリシーを変更できるものとします。

    変更後の内容は、本ページへ掲載した時点から効力を生じます。

