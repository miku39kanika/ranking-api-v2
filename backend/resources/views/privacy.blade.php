# プライバシーポリシー

最終更新日：2026年5月27日

「Mikuち」（以下「運営者」）は、「なんでも！ランキング」（以下「本サービス」）におけるユーザー情報の取扱いについて、以下のとおりプライバシーポリシーを定めます。

---

# 1. 取得する情報

運営者は、本サービスにおいて以下の情報を取得する場合があります。

* ユーザーID
* ユーザー名
* プロフィール情報
* アイコン情報
* 投稿したランキング情報
* 投票履歴
* 招待情報
* 端末識別情報
* 広告識別子
* ログ情報
* お問い合わせ情報

---

# 2. 利用目的

取得した情報は、以下の目的で利用します。

* 本サービス提供
* 不正利用防止
* ランキング機能提供
* 広告表示
* 問い合わせ対応
* サービス改善
* 障害対応
* 利用状況分析

---

# 3. 広告について

本サービスでは、Google AdMob等の広告サービスを利用する場合があります。

広告配信事業者は、ユーザーの広告識別子等を利用する場合があります。

ユーザーは、端末設定等により広告トラッキングを制限できる場合があります。

---

# 4. 外部サービス・外部API

本サービスでは、以下の外部サービスまたはAPIを利用する場合があります。

* Google AdMob
* ふりがな変換API（[https://api.excelapi.org/language/kanji2kana）](https://api.excelapi.org/language/kanji2kana）)

これらの外部サービスでは、必要な範囲で情報が送信される場合があります。

---

# 5. 情報の第三者提供

運営者は、以下の場合を除き、ユーザー情報を第三者へ提供しません。

* ユーザー本人の同意がある場合
* 法令に基づく場合
* 人命・財産保護のため必要な場合
* 不正利用防止のため必要な場合

---

# 6. 情報の管理

運営者は、ユーザー情報の漏えい、滅失、毀損等を防止するため、合理的な安全対策を講じます。

---

# 7. アカウント削除

ユーザーは、運営者所定の方法によりアカウント削除を申請できます。

削除後、一部情報は法令対応や不正利用防止のため一定期間保持される場合があります。

---

# 8. 未成年者について

未成年者が本サービスを利用する場合、保護者の同意を得たうえで利用するものとします。

---

# 9. ポリシーの変更

運営者は、必要に応じて本ポリシーを変更できるものとします。

変更後の内容は、本サービスまたは関連サイトへ掲載した時点から効力を生じます。

---

